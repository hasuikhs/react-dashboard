CREATE TABLE  tb_user (
  `seq` INT NOT NULL AUTO_INCREMENT COMMENT '시퀀스',
  `user_nm` VARCHAR(64) NOT NULL DEFAULT '' COMMENT '유저명',
  `user_id` VARCHAR(64) NOT NULL DEFAULT '' COMMENT '유저 ID'
  `user_pw` VARCHAR(128) NOT NULL '' COMMENT '비밀번호'
  `login_dt` DATETIME NOT NULL DEFAULT '2022-01-01 00:00:00' COMMENT '마지막 로그인',
  `reg_dt` DATETIME NOT NULL DEFAULT '2022-01-01 00:00:00' COMMENT '등록일',
  `upd_dt` DATETIME NOT NULL DEFAULT '2022-01-01 00:00:00' COMMENT '수정일',
  PRIMARY KEY (`seq`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_0900_ai_ci;